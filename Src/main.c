// uart data receive
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(huart == &huart1){
	  c = atoi ((char*)&rxTemBuf[0]);
	  sprintf((char*)rxTemBuf,"%d\n",c);
	  switch(c){
	  case 11: //doldur basla
		  flagRX = 1;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, GPIO_PIN_RESET);
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_SET);
		  break;
	  case 10:// doldur dur
		  flagRX = 0;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_RESET);
		  break;
	  case 21: // bosalt basla
		  flagRX = 1;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_RESET);
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, GPIO_PIN_SET);
		  break;
	  case 20:// bosalt dur
		  flagRX = 0;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, GPIO_PIN_RESET);
		  break;
	  case 31:
		  flagRX=1;
		  LightSystemSet(1, 100);
		  LightSystemSent();
		  break;
	  case 32:
		  flagRX=1;
		  LightSystemSet(2, 100);
		  LightSystemSent();
		  break;
	  case 33:
		  flagRX=1;
		  LightSystemSet(3, 100);
		  LightSystemSent();
		  break;
	  case 30:
		  flagRX=0;
		  LightSystemSet(0, 100);
		  LightSystemSent();
		  break;
	  case 41:
		  flagRX=1;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_SET);
		  break;
	  case 40:
		  flagRX=0;
		  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_RESET);
		  break;
	  }
	  c=0;
	  rxTemBuf[0] = 0;
	  HAL_UART_Receive_IT(&huart1, rxTemBuf,2);
  }
}
