void vent(float tempV){
	if(tempV>30.0){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_SET);
	}
	else if(tempV<17.0){
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_12, GPIO_PIN_RESET);
	}
}
//FUNCTION FOR DATA Transmit
void getSV(){
		  if(flagS){
	 	  dk = sTime.Minutes;
		  st = sTime.Hours;
		  airQ = 100*(data[0]/4095.0);
		  wL = 100*(data[1]/4095.0);
		  pres =getBMP(1);
		  alt = getBMP(2);
		  temp = getTemp();
		  hum = getHum();
		  sprintf((char*)bufferTX,"%.1f, %.1f, %.1f, %.1f, %.1f, %.1f\n",pres,alt,temp,hum,airQ,wL);
		  HAL_UART_Transmit(&huart1, (uint8_t*)bufferTX, (size_t)strlen((char*)bufferTX), HAL_MAX_DELAY);
		  if(flagRX==0){
			  vent(temp);
			  if(10<dk && dk<=17){
				  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_SET);
			  }
			  else if(17<dk && dk<=24){
				  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, GPIO_PIN_SET);
				  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_RESET);
			  }
			  else if(dk<=10 || dk>25){
				  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_11, GPIO_PIN_RESET);
				  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_10, GPIO_PIN_RESET);
			  }
		  }
	}
		  flagS=0;
}

